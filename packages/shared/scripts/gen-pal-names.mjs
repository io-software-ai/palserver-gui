import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const here = path.dirname(fileURLToPath(import.meta.url));
const palSource = path.resolve(here, "../../web/public/game-data/pals.json");
const humanSource = path.resolve(here, "../../web/public/game-data/humans.json");
const target = path.resolve(here, "../src/pal-names.generated.ts");
const toRow = (entry) => [
  entry.id,
  entry.zh || entry.name || entry.id,
  entry["zh-CN"] || entry.zhCN || entry.zh || entry.name || entry.id,
  entry.name || entry.id,
  entry.ja || entry.name || entry.id,
];
const palRows = JSON.parse(fs.readFileSync(palSource, "utf8")).map(toRow);
const humanRows = JSON.parse(fs.readFileSync(humanSource, "utf8")).map(toRow);

const output = `// Generated by scripts/gen-pal-names.mjs from web/public/game-data/{pals,humans}.json.\n` +
  `import type { MessageBridgeLanguage } from "./index.js";\n\n` +
  `type NameRow = readonly [string, string, string, string, string];\n` +
  `const PAL_NAMES: ReadonlyArray<NameRow> = ${JSON.stringify(palRows)};\n` +
  `const HUMAN_NAMES: ReadonlyArray<NameRow> = ${JSON.stringify(humanRows)};\n\n` +
  `const LANGUAGE_INDEX: Record<MessageBridgeLanguage, number> = { "zh-TW": 1, "zh-CN": 2, en: 3, ja: 4 };\n` +
  `const LOOKUP = new Map<string, NameRow>();\n` +
  `for (const row of PAL_NAMES) for (const value of row) LOOKUP.set(value.toLocaleLowerCase(), row);\n\n` +
  `for (const row of HUMAN_NAMES) LOOKUP.set(row[0].toLocaleLowerCase(), row);\n\n` +
  `export function localizePalName(value: string, language: MessageBridgeLanguage): string {\n` +
  `  const clean = value.trim();\n` +
  `  const withoutBoss = clean.replace(/^BOSS_/i, "");\n` +
  `  const baseId = withoutBoss.replace(/_otomo$/i, "");\n` +
  `  const row = [clean, withoutBoss, baseId].map((candidate) => LOOKUP.get(candidate.toLocaleLowerCase())).find(Boolean);\n` +
  `  return row?.[LANGUAGE_INDEX[language]] || clean;\n` +
  `}\n`;

fs.writeFileSync(target, output);
console.log(`Generated ${palRows.length} Pal and ${humanRows.length} human name rows in ${target}`);
