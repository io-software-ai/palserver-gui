import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const here = path.dirname(fileURLToPath(import.meta.url));
const source = path.resolve(here, "../../web/public/game-data/pals.json");
const target = path.resolve(here, "../src/pal-names.generated.ts");
const pals = JSON.parse(fs.readFileSync(source, "utf8"));
const rows = pals.map((pal) => [
  pal.id,
  pal.zh || pal.name || pal.id,
  pal["zh-CN"] || pal.zhCN || pal.zh || pal.name || pal.id,
  pal.name || pal.id,
  pal.ja || pal.name || pal.id,
]);

const output = `// Generated by scripts/gen-pal-names.mjs from web/public/game-data/pals.json.\n` +
  `import type { MessageBridgeLanguage } from "./index.js";\n\n` +
  `const PAL_NAMES: ReadonlyArray<readonly [string, string, string, string, string]> = ${JSON.stringify(rows)};\n\n` +
  `const LANGUAGE_INDEX: Record<MessageBridgeLanguage, number> = { "zh-TW": 1, "zh-CN": 2, en: 3, ja: 4 };\n` +
  `const LOOKUP = new Map<string, (typeof PAL_NAMES)[number]>();\n` +
  `for (const row of PAL_NAMES) for (const value of row) LOOKUP.set(value.toLocaleLowerCase(), row);\n\n` +
  `export function localizePalName(value: string, language: MessageBridgeLanguage): string {\n` +
  `  const clean = value.trim();\n` +
  `  const withoutBoss = clean.replace(/^BOSS_/i, "");\n` +
  `  const row = LOOKUP.get(clean.toLocaleLowerCase()) ?? LOOKUP.get(withoutBoss.toLocaleLowerCase());\n` +
  `  return row?.[LANGUAGE_INDEX[language]] || clean;\n` +
  `}\n`;

fs.writeFileSync(target, output);
console.log(`Generated ${rows.length} Pal name rows in ${target}`);
